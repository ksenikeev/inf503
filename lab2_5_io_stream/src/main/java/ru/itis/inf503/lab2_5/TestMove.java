public class TestMove {
    public static void main(String[] args) {
        int r = 3; // 11

        System.out.println(r << 1); // 110
        System.out.println(r << 2); // 1100

        System.out.println(r << 8); // 1100000000

        System.out.println(387 >> 1); // 1100000011

        System.out.println(-34 >> 1);
    }
}
